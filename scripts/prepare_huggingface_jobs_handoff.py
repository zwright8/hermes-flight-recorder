def _validate_model_manifest(model_manifest: dict[str, Any]) -> None:
    if model_manifest.get("schema_version") != "hfr.model_candidate.v1":
        raise ValueError("model manifest must use hfr.model_candidate.v1")
    if not model_manifest.get("candidate_id") or not model_manifest.get("model_id"):
        raise ValueError("model manifest must declare candidate_id and model_id")
    source = model_manifest.get("source") if isinstance(model_manifest.get("source"), dict) else {}
    revision = str(source.get("revision") or "")
    if len(revision) < 40 or not all(char in "0123456789abcdef" for char in revision):
        raise ValueError("model manifest source.revision must be an immutable commit")
    license_record = model_manifest.get("license") if isinstance(model_manifest.get("license"), dict) else {}
    if (
        license_record.get("status") != "approved"
        or license_record.get("review_status") != "approved"
        or license_record.get("accepted_terms") is not True
        or license_record.get("training_allowed") is not True
    ):
        raise ValueError("model manifest license must be explicitly reviewed and approved for training")
    compatibility = model_manifest.get("compatibility") if isinstance(model_manifest.get("compatibility"), dict) else {}
    tokenizer = compatibility.get("tokenizer") if isinstance(compatibility.get("tokenizer"), dict) else {}
    chat_template = compatibility.get("chat_template") if isinstance(compatibility.get("chat_template"), dict) else {}
    tokenizer_revision = str(tokenizer.get("revision") or "")
    if len(tokenizer_revision) < 40 or not all(char in "0123456789abcdef" for char in tokenizer_revision):
        raise ValueError("model manifest compatibility.tokenizer.revision must be an immutable commit")
    chat_template_sha256 = str(chat_template.get("sha256") or "")
    if len(chat_template_sha256) != 64 or not all(char in "0123456789abcdef" for char in chat_template_sha256):
        raise ValueError("model manifest compatibility.chat_template.sha256 is required")


def _validate_source_plan(
    plan: dict[str, Any],
    model_manifest: dict[str, Any],
    dataset_manifest: dict[str, Any],
    mode: str | None,
) -> None:
    if plan.get("schema_version") != "hfr.agentic_lora_training_plan.v1":
        raise ValueError("--training-plan must be an hfr.agentic_lora_training_plan.v1 artifact")
    if plan.get("passed") is not True or plan.get("recommendation") != "launch_allowed":
        raise ValueError("--training-plan must be passing and launch_allowed")
    if plan.get("model") != model_manifest.get("model_id"):
        raise ValueError("training plan model must match the immutable model manifest")
    if mode and mode != plan.get("mode"):
        raise ValueError("--mode must match the reviewed training plan")
    tracking = plan.get("tracking") if isinstance(plan.get("tracking"), dict) else {}
    if tracking.get("report_to") != ["trackio"]:
        raise ValueError("reviewed training plan must enable Trackio")
    if str(plan.get("mode") or "").startswith("fr_"):
        evidence = plan.get("control_evidence") if isinstance(plan.get("control_evidence"), dict) else {}
        artifacts = evidence.get("artifacts") if isinstance(evidence.get("artifacts"), dict) else {}
        expected_controls = {
            "governance_receipt", "contamination_report", "curated_dataset",
            "action_credit", "branch_replay", "reviewed_preferences",
        }
        if (
            evidence.get("required") is not True
            or evidence.get("passed") is not True
            or evidence.get("failures") != []
            or set(artifacts) != expected_controls
        ):
            raise ValueError("Flight Recorder source plan must carry replayed content-bound control evidence")
        checks = {
            str(row.get("id")): row.get("passed")
            for row in plan.get("checks", [])
            if isinstance(row, dict)
        }
        if checks.get("content_bound_control_artifacts_replayed") is not True or checks.get(
            "training_rows_bound_to_control_artifacts"
        ) is not True:
            raise ValueError("Flight Recorder source plan did not pass content and row control replay")
        manifest_records = dataset_manifest.get("artifact_fingerprints")
        if not isinstance(manifest_records, dict):
            raise ValueError("dataset manifest has no artifact fingerprints")
        for name in sorted(expected_controls):
            plan_record = artifacts.get(name) if isinstance(artifacts.get(name), dict) else {}
            manifest_record = manifest_records.get(name) if isinstance(manifest_records.get(name), dict) else {}
            if (
                plan_record.get("sha256") != manifest_record.get("sha256")
                or plan_record.get("size_bytes") != manifest_record.get("size_bytes")
            ):
                raise ValueError(f"source plan control evidence does not match dataset manifest: {name}")


def prepare(args: argparse.Namespace) -> dict[str, Any]:
    experiment = args.experiment_dir.resolve()
    dataset_manifest_path = experiment / "dataset_training_manifest.json"
    if not dataset_manifest_path.exists():
        raise ValueError("experiment is missing dataset_training_manifest.json; run build_agentic_finetune_experiment.py first")
    source_plan_path = args.training_plan.resolve()
    model_manifest_path = args.model_manifest.resolve()
    source_plan = read_json(source_plan_path)
    model_manifest = read_json(model_manifest_path)
    dataset_manifest = read_json(dataset_manifest_path)
    _validate_model_manifest(model_manifest)
    _validate_source_plan(source_plan, model_manifest, dataset_manifest, args.mode)
    dependencies = list(args.dependency or EXACT_DEPENDENCIES)

    out = args.out.resolve()
    if out.exists() and any(out.iterdir()):
        raise ValueError(f"output directory is not empty: {out}")
    payload = out / "payload"
    (payload / "runtime").mkdir(parents=True, exist_ok=True)
    (payload / "reviewed").mkdir(parents=True, exist_ok=True)
    shutil.copytree(experiment / "data", payload / "data")
    controls_source = experiment / "controls"
    if not controls_source.is_dir():
        raise ValueError("experiment is missing content-bound controls required by Flight Recorder training")
    shutil.copytree(controls_source, payload / "controls")
    sanitized_dataset_manifest = dataset_manifest
    sanitized_dataset_manifest.pop("source_manifest", None)
    write_json(payload / "dataset_training_manifest.json", sanitized_dataset_manifest)
    write_json(payload / "model_manifest.json", model_manifest)
    shutil.copy2(TRAINER, payload / "runtime" / "train_agentic_lora.py")
    shutil.copy2(LIFECYCLE_MODULE, payload / "runtime" / "huggingface_lifecycle.py")
    write_json(payload / "reviewed" / "source_training_plan.json", source_plan)

    data_artifacts = [
        file_record(path, base_dir=payload, role="training_data")
        for directory in (payload / "data", payload / "controls")
        for path in sorted(directory.rglob("*"))
        if path.is_file()
    ]
    reviewed_plan = build_reviewed_training_plan(
        source_plan=source_plan,
        source_plan_record=file_record(
            payload / "reviewed" / "source_training_plan.json", base_dir=payload, role="source_training_plan"
        ),
        model_manifest=model_manifest,
        model_manifest_record=file_record(payload / "model_manifest.json", base_dir=payload, role="model_manifest"),
        dataset_manifest_record=file_record(
            payload / "dataset_training_manifest.json", base_dir=payload, role="dataset_manifest"
        ),
        trainer_script_record=file_record(
            payload / "runtime" / "train_agentic_lora.py", base_dir=payload, role="trainer_script"
        ),
        data_artifacts=data_artifacts,
        reviewer=args.reviewer,
        reviewed_at=args.reviewed_at or utc_now(),
        dataset_repo=args.dataset_repo,
        model_repo=args.hub_model_id,
        flavor=args.flavor,
        timeout=args.timeout,
        container_image=args.container_image,
        dependencies=dependencies,
        private=args.private,
        resume={
            "enabled": bool(args.resume_revision),
            "phase": args.resume_phase,
            "revision": args.resume_revision,
            "checkpoint_path": args.resume_checkpoint_path,
            "checkpoint_manifest_sha256": args.resume_checkpoint_manifest_sha256,
        },
    )
    plan_path = payload / "reviewed_training_plan.json"
    write_json(plan_path, reviewed_plan)
    plan_errors = validate_reviewed_training_plan(reviewed_plan, payload)
    if plan_errors:
        raise HuggingFaceLifecycleError("reviewed plan did not replay after packaging: " + "; ".join(plan_errors))
    (payload / "runtime" / "hf_job.py").write_text(job_script(args.dataset_repo, dependencies), encoding="utf-8")

    payload_records = [
        file_record(path, base_dir=payload, role="payload")
        for path in sorted(payload.rglob("*"))
        if path.is_file()
    ]
    write_json(
        payload / "payload_manifest.json",
        {
            "schema_version": "hfr.huggingface_payload_manifest.v1",
            "artifact_count": len(payload_records),
            "artifact_set_sha256": artifact_set_sha256(payload_records),
            "artifacts": payload_records,
        },
    )
    payload_records.append(file_record(payload / "payload_manifest.json", base_dir=payload, role="payload_manifest"))
    plan_sha256 = sha256_file(plan_path)
    request = {
        "script": f"https://huggingface.co/datasets/{args.dataset_repo}/resolve/REPLACE_WITH_DATASET_COMMIT/runtime/hf_job.py",
        "script_args": [
            "--dataset-revision",
            "REPLACE_WITH_DATASET_COMMIT",
            "--reviewed-plan",
            "reviewed_training_plan.json",
            "--reviewed-plan-sha256",
            plan_sha256,
        ],
        "flavor": reviewed_plan["runtime"]["hardware_flavor"],
        "timeout": reviewed_plan["runtime"]["timeout"],
        "image": reviewed_plan["runtime"]["container_image"],
        "secrets": {"HF_TOKEN": "$HF_TOKEN"},
    }
    write_json(out / "job_request.template.json", request)
    (out / "upload_to_hub.py").write_text(upload_script(args.dataset_repo, args.private), encoding="utf-8")

    handoff = attach_identity(
        {
            "schema_version": HF_JOBS_HANDOFF_SCHEMA_VERSION,
            "created_at": utc_now(),
            "provider": "huggingface_jobs",
            "submitted": False,
            "network_writes_performed": False,
            "reviewed_plan": file_record(plan_path, base_dir=out, role="reviewed_training_plan"),
            "reviewed_plan_identity": reviewed_plan["identity"],
            "payload_artifact_count": len(payload_records),
            "payload_artifact_set_sha256": artifact_set_sha256(payload_records),
            "payload_artifacts": payload_records,
            "runtime": reviewed_plan["runtime"],
            "output": reviewed_plan["output"],
            "resume": reviewed_plan["resume"],
            "upload_command": f"uv run {out / 'upload_to_hub.py'}",
            "submission": {
                "tool": "hf_jobs",
                "operation": "uv",
                "request_template": str(out / "job_request.template.json"),
                "request_after_upload": str(out / "job_request.json"),
                "requires_explicit_paid_network_approval": True,
            },
            "canonical_completion": {
                "remote_path": reviewed_plan["output"]["result_path"],
                "import_requires_immutable_completion_revision": True,
                "canonical_artifacts": [
                    "hfr.agentic_training_result.v1",
                    "hfr.cloud_training_completion_receipt.v1",
                ],
            },
            "checks": {
                "reviewed_plan_replayed": True,
                "trainer_arguments_derive_only_from_plan": True,
                "immutable_model_tokenizer_and_template": True,
                "dependencies_exactly_locked": True,
                "private_dataset_and_model_repositories": args.private is True,
                "checkpoint_and_final_push_enabled": True,
                "durable_terminal_receipt_required": True,
                "observed_cuda_driver_runtime_and_libraries_required": True,
                "reviewed_plan_path_hash_replay_required": True,
            },
        }
    )
    write_json(out / "handoff.json", handoff)
    (out / "README.md").write_text(
        "# Hugging Face Jobs handoff\n\n"
        "1. Review `payload/reviewed_training_plan.json`, `payload/`, and `handoff.json`.\n"
        "2. With explicit approval, run the recorded `upload_command`.\n"
        "3. Submit `job_request.json` through the Hugging Face Jobs integration.\n"
        "4. Import the immutable remote completion receipt; never treat job exit alone as success.\n"
        "5. Completion and publication receipts must replay the reviewed plan and attest observed "
        "CUDA, driver, runtime, and library identities.\n",
        encoding="utf-8",
    )
    return handoff


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--experiment-dir", type=Path, required=True)
    parser.add_argument("--training-plan", type=Path, required=True)
    parser.add_argument("--model-manifest", type=Path, required=True)
    parser.add_argument("--reviewer", required=True)
    parser.add_argument("--reviewed-at", default="")
    parser.add_argument("--dataset-repo", required=True, help="Private Hugging Face dataset repo")
    parser.add_argument("--hub-model-id", required=True, help="Private Hub model repo for checkpoints, adapter, and receipts")
    parser.add_argument("--mode", choices=sorted({"trace_sft", "fr_sft", "fr_action_sft", "fr_dpo", "fr_sft_dpo"}))
    parser.add_argument("--flavor", default="a10g-large")
    parser.add_argument("--timeout", default="4h")
    parser.add_argument(
        "--container-image",
        required=True,
        help="Execution image pinned as <registry>/<image>@sha256:<digest>",
    )
    parser.add_argument("--dependency", action="append", default=[])
    parser.add_argument("--private", action=argparse.BooleanOptionalAction, default=True)
    parser.add_argument("--resume-revision", default="")
    parser.add_argument("--resume-phase", choices=["", "sft", "dpo"], default="")
    parser.add_argument("--resume-checkpoint-path", default="")
    parser.add_argument("--resume-checkpoint-manifest-sha256", default="")
    parser.add_argument("--out", type=Path, required=True)
    return parser.parse_args()


def main() -> int:
    try:
        handoff = prepare(parse_args())
    except (HuggingFaceLifecycleError, OSError, ValueError, json.JSONDecodeError) as exc:
        raise SystemExit(str(exc)) from exc
    print(json.dumps(handoff, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
