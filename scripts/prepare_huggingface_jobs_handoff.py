def artifact_record(path, root, role, repo_prefix):
    row = file_record(path, base_dir=root, role=role)
    row["path"] = f"{repo_prefix}/{row['path']}"
    return row


def materialize_resume(plan, model_repo, work_root):
    resume = plan["resume"]
    if resume["enabled"] is not True:
        return None
    snapshot = Path(
        snapshot_download(
            repo_id=model_repo,
            repo_type="model",
            revision=resume["revision"],
        )
    )
    checkpoint = snapshot / resume["checkpoint_path"]
    manifest = checkpoint / "trainer_state.json"
    if not checkpoint.exists() or not checkpoint.is_dir():
        raise HuggingFaceLifecycleError("resume checkpoint directory is missing")
    if not manifest.exists() or sha256_file(manifest) != resume["checkpoint_manifest_sha256"]:
        raise HuggingFaceLifecycleError("resume checkpoint manifest fingerprint mismatch")
    target = work_root / "resume_checkpoint"
    shutil.copytree(checkpoint, target)
    return target


